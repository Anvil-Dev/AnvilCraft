package dev.dubhe.anvilcraft.item;

import com.google.common.base.Suppliers;
import dev.dubhe.anvilcraft.init.ModItems;
import net.minecraft.world.item.Items;
import net.minecraft.world.item.Tier;
import net.minecraft.world.item.crafting.Ingredient;
import org.jetbrains.annotations.NotNull;

import java.util.function.Supplier;

public enum ModTiers implements Tier {
    AMETHYST(1, 751, 4.0f, 1.0f, 10, () -> Ingredient.of(Items.AMETHYST_SHARD)),
    EMBER_METAL(5, 0, 10.0f, 1.0f, 22, () -> Ingredient.of(ModItems.EMBER_METAL_INGOT));
    private final int level;
    private final int uses;
    private final float speed;
    private final float damage;
    private final int enchantmentValue;
    private final Supplier<Ingredient> repairIngredient;

    ModTiers(
        int level, int uses, float speed, float damage, int enchantmentValue, @NotNull Supplier<Ingredient> supplier
    ) {
        this.level = level;
        this.uses = uses;
        this.speed = speed;
        this.damage = damage;
        this.enchantmentValue = enchantmentValue;
        this.repairIngredient = Suppliers.memoize(supplier::get);
    }

    @Override
    public int getUses() {
        return this.uses;
    }

    @Override
    public float getSpeed() {
        return this.speed;
    }

    @Override
    public float getAttackDamageBonus() {
        return this.damage;
    }

    @Override
    public int getLevel() {
        return this.level;
    }

    @Override
    public int getEnchantmentValue() {
        return this.enchantmentValue;
    }

    @Override
    public @NotNull Ingredient getRepairIngredient() {
        return this.repairIngredient.get();
    }
}
